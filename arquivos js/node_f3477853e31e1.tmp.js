/** ESTRUTURAS DE REPETIÇÃO**/

var contador = 1
/** Laço de repetição com teste lógico no INÍCIO.**/
while (contador <= 4) {
    console.log(`Bom dia!`)
    console.log(`Passo ${contador}`)
    contador++  //Vai aumentando o contador progressivamente.
}

var contador2 = 1
/** Laço de repetição com teste lógico no FINAL.**/
do {
    console.log(`Bom dia!! ${contador2}`)
    contador2++
} while (contador2 = 16)


